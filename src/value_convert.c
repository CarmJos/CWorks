#include <stdio.h>


int main() {

    char c1, c2;

    c1 = 97;
    c2 = 98;
    printf("%d,%d", c1, c2);
    printf("\n");
    printf("%c,%c", c1, c2);
    printf("\n\n");

    c1 = 197;
    c2 = 198;
    printf("%d,%d", c1, c2);
    printf("\n");
    printf("%c,%c", c1, c2);
    printf("\n\n");

    int v1, v2;
    v1 = 97;
    v2 = 98;
    printf("%d,%d", v1, v2);
    printf("\n");
    printf("%c,%c", v1, v2);
    printf("\n\n");

    v1 = 197;
    v2 = 198;
    printf("%d,%d", v1, v2);
    printf("\n");
    printf("%c,%c", v1, v2);
    printf("\n\n");

    return 0;
}
